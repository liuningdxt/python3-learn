"""
    运算符
    赋值运算符
    算数运算符
"""

# 为变量age赋值18
age = 18

# 将变量age的值赋值给变量age2
age2 = age

# 将表达式值赋值给age3
age3 = age2 + 1

print("age=", age)  # ctrl + d 复制多行
print("age2=", age2)
print("age3=", age3)

a = 10
b = 21
c = 21
print("a+b=", a + b)
print("a-b=", a - b)
print("a*b=", a * b)
print("b/a=", b / a)
print("a**b=", a ** b)
print("b//a=", b // a)
print("b%a=", b % a)

# c+=a 等同于 c=c+a
# c += a
print("c+=a c=", c)

# c-=a 等同于 c=c-a
# c -= a
print("c-=a c=", c)

# c*=a 等同于 c=c*a
# c *= a
print("c*=a c=", c)

# c/=a 等同于 c=c/a
c /= a
print("c/=a c=", c)
